/* Declare the main method, Initialize the count, and the condition of greater than equal to zero, the decrement factor and then print out*/



public class NumberFiftyToOne {

	public static void main(String[] args) {

	
	for (int count = 50; count > 0; count--){
		
		System.out.printf("%d ", count);
	
	}
	
	

		System.out.println();
}		

}