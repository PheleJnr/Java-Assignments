/* Declare the main method, Initialize the number, and the condition of less than equal to Ten, the increment factor, then the product of the numbers from 1 to 10 and then print out*/

public class MultiplicationOfNine {

	public static void main(String[] args) {

	int product = 9;

	int number = 1;

	for(number = 1; number <= 12; number++){
		product *= number;
		
		}
		product++;
		System.out.printf("The Product of Numbers is %d ", product);
}

}