public class Triangle4 {

	public static void main(String[] args) {

	

	for (int count = 1; count <= 10; count++) {
		for (int value = 10; value >= count; value--) {
			System.out.print(" ");
		}
	for (int number = 10; number >= count; number--){
		System.out.print("*");
	}

		System.out.println();
}
}

}